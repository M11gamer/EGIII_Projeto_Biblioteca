
package model.bean;

public class Livros {
    
private int livros_id;
private String livros_titulo;
private int livros_numpag; 
private String livros_editora; 
private String livros_autor; 
private String livros_descricao; 
private int livros_quantidade;
private int livros_sessao;

    public int getLivros_id() {
        return livros_id;
    }

    public void setLivros_id(int livros_id) {
        this.livros_id = livros_id;
    }

    public String getLivros_titulo() {
        return livros_titulo;
    }

    public void setLivros_titulo(String livros_titulo) {
        this.livros_titulo = livros_titulo;
    }

    public int getLivros_numpag() {
        return livros_numpag;
    }

    public void setLivros_numpag(int livros_numpag) {
        this.livros_numpag = livros_numpag;
    }

    public String getLivros_editora() {
        return livros_editora;
    }

    public void setLivros_editora(String livros_editora) {
        this.livros_editora = livros_editora;
    }

    public String getLivros_autor() {
        return livros_autor;
    }

    public void setLivros_autor(String livros_autor) {
        this.livros_autor = livros_autor;
    }

    public String getLivros_descricao() {
        return livros_descricao;
    }

    public void setLivros_descricao(String livros_descricao) {
        this.livros_descricao = livros_descricao;
    }

    public int getLivros_quantidade() {
        return livros_quantidade;
    }

    public void setLivros_quantidade(int livros_quantidade) {
        this.livros_quantidade = livros_quantidade;
    }

    public int getLivros_sessao() {
        return livros_sessao;
    }

    public void setLivros_sessao(int livros_sessao) {
        this.livros_sessao = livros_sessao;
    }
    

}
