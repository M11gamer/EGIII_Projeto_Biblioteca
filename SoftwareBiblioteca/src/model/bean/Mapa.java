
package model.bean;


public class Mapa {
    
    private int mapa_id;
    private String mapa_nome;
    private String mapa_descricao;
    private String mapa_sessao;

    public int getMapa_id() {
        return mapa_id;
    }

    public void setMapa_id(int mapa_id) {
        this.mapa_id = mapa_id;
    }

    public String getMapa_nome() {
        return mapa_nome;
    }

    public void setMapa_nome(String mapa_nome) {
        this.mapa_nome = mapa_nome;
    }

    public String getMapa_descricao() {
        return mapa_descricao;
    }

    public void setMapa_descricao(String mapa_descricao) {
        this.mapa_descricao = mapa_descricao;
    }

    public String getMapa_sessao() {
        return mapa_sessao;
    }

    public void setMapa_sessao(String mapa_sessao) {
        this.mapa_sessao = mapa_sessao;
    }
    
    
    
}
