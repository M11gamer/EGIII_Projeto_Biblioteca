
package model.bean;


public class Sessoes {

private int sessoes_id;
private String sessoes_num;
private String sessoes_descricao;

    public int getSessoes_id() {
        return sessoes_id;
    }

    public void setSessoes_id(int sessoes_id) {
        this.sessoes_id = sessoes_id;
    }

    public String getSessoes_num() {
        return sessoes_num;
    }

    public void setSessoes_num(String sessoes_num) {
        this.sessoes_num = sessoes_num;
    }

    public String getSessoes_descricao() {
        return sessoes_descricao;
    }

    public void setSessoes_descricao(String sessoes_descricao) {
        this.sessoes_descricao = sessoes_descricao;
    }



}
